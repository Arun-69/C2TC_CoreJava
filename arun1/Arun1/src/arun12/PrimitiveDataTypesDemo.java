package arun12;

public class PrimitiveDataTypesDemo {
	public static void main(String args[]) {
		byte byteMax=127;
		byte byteMin=-128;
		System.out.println("Min range of byte is"+ byteMin+"Max range of is"+byteMax);
		short shortMax=32767;
		short shortMin=-32768;
		System.out.println("Minshort range of byte is"+shortMin+"Maxshort range byte is"+shortMax);
		int maxInt=2147483647;
		int minInt=-2147483648;
		System.out.println("Minint range of byte is"+minInt+"Maxint range byte is"+maxInt);
		long maxLong=9223372036854775807L;
		long minLong=-9223372036854775807L;
		System.out.println("Minlong range of byte is"+minLong+"Maxlong range of byte is"+maxLong);
		float f=3234.141243278345f;
		double d=3456.1234678998765432f;
		System.out.println("float value is"+f+"double value is"+d);
		boolean flag=false;
		System.out.println("boolean value is"+flag);
		
	}

}
